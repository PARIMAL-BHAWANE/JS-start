// ********************************************************************* Notes *********************************************************************

// 1. == (Equality) => It will compare the values of the operands and return true if they are equal.
// 2. === (Identity) => It will compare the values and the type of the operands and return true if they are equal.
// 3. != (Inequality) => It will compare the values of the operands and return true if they are not equal.
// 4. !== (Non-identity) => It will compare the values and the type of the operands and return true if they are not equal.
// 5. > (Greater than) => It will return true if the left operand is greater than the right operand.
// 6. < (Less than) => It will return true if the left operand is less than the right operand.
// 7. >= (Greater than or equal to) => It will return true if the left operand is greater than or equal to the right operand.
// 8. <= (Less than or equal to) => It will return true if the left operand is less than or equal to the right operand.



